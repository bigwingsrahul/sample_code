// Enum for different stages of delivery process
enum DispatchStage {
  headedToPickup,
  checkedInPickup,
  pickedUpLoad,
  headedToDropOff,
  checkedInDropOff,
  deliveredLoad,
  headedToReDeliver,
  checkedInReDeliver,
  redelivered,
  headedToDumpAndDonate,
  checkedInDumpAndDonate,
  dumpAndDonated,
}
